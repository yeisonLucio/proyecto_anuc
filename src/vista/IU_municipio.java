    private void btnGuardarVeredaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarVeredaActionPerformed
         if(comboMunicipio.getSelectedItem()=="--SELECCIONAR--"){
            JOptionPane.showMessageDialog(null,"SELECCIONE UN MUNICIPIO");
        }else{
        
        
        try {
            if(esModificarVer==false){
                //guardar
                bd.insertarVereda(cod_municipio, txtVereda.getText());
                JOptionPane.showMessageDialog(null, "GUARDADO CORRECTAMENTE");
                llenarListaYtabla();
                txtVereda.setText(null);
            }else{
                //modificar
                bd.modificarVereda(getIDvereda(), txtVereda.getText(), cod_municipio);
                JOptionPane.showMessageDialog(null, "MODIFICADO CORRECTAMENTE");
                llenarListaYtabla();
                txtVereda.setText(null);
            }
            BtnEliminarVereda.setEnabled(false);
            esModificarVer=false;
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(null,"error al guardar vereda" +ex);
        }
      }
    }//GEN-LAST:event_btnGuardarVeredaActionPerformed

