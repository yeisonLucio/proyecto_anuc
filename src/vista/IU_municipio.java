    private int getIDvereda(){
        int r=0;
        try {
            Statement stmt = bd.getConexion(gys.getHost(), gys.getNomBD(), gys.getUser(), gys.getPassword()).createStatement();
            ResultSet rs=stmt.executeQuery("select v.idvereda from vereda v where nombre = '"+tablaMunicipio.getValueAt(tablaMunicipio.getSelectedRow(), 1).toString()+"'");
            while(rs.next())
                r = rs.getInt("idvereda");
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(null,"error \n" +ex); 
           
        }
        return r;
    }
    private int getIDmunicipio(){
        int r=0;
         try {
            Statement stmt = bd.getConexion(gys.getHost(), gys.getNomBD(), gys.getUser(), gys.getPassword()).createStatement();
            ResultSet rs=stmt.executeQuery("select idmunicipio from municipio  where nombre = '"+tablaMunicipio.getValueAt(tablaMunicipio.getSelectedRow(), 0).toString()+"'");
            while(rs.next())
                r = rs.getInt("idmunicipio");
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(null,"error \n" +ex); 
           
        }
        return r;
        
    }

